// Vetores:

// vantagens - os vetores incluem acesso rápido e direto aos elementos, o que os torna 
// eficientes para operações de leitura quando o tamanho é fixo. Eles também são simples de usar 
// e possuem alocação contínua de memória, otimizando o cache.

// desvantagens - incluem tamanho fixo, o que pode desperdiçar memória ou ser insuficiente, 
// além de serem ineficientes para inserções e remoções frequentes, já que essas operações exigem o 
// deslocamento de elementos.


// ponteiros:

// vantagens - como flexibilidade na alocação dinâmica de memória, 
// sendo ideais para estruturas de dados dinâmicas, como listas encadeadas e árvores. 

// desvantagens - incluem maior complexidade, necessidade de gerenciamento manual de memória 
// e maior propensão a erros, como vazamentos de memória e acessos inválidos.